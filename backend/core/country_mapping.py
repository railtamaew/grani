# Обратный маппинг для быстрого поиска
CODE_TO_COUNTRY: Dict[str, str] = {v: k for k, v in COUNTRY_TO_CODE.items()}


def get_country_code(country: str) -> Optional[str]:
    """
    Получить ISO код страны по названию.
    
    Args:
        country: Название страны (на русском или английском)
    
    Returns:
        ISO код страны или None если не найдено
    """
    # Нормализуем название страны
    country_normalized = country.strip()
    
    # Прямой поиск
    code = COUNTRY_TO_CODE.get(country_normalized)
    if code:
        return code
    
    # Поиск без учета регистра
    country_lower = country_normalized.lower()
    for key, value in COUNTRY_TO_CODE.items():
        if key.lower() == country_lower:
            return value
    
    # Если не найдено, возвращаем None
    return None


def generate_flag_url(country: str) -> str:
    """
    Генерирует URL флага страны на основе названия страны.
    Использует сервис flagcdn.com для получения флагов.
    
    Args:
        country: Название страны
    
    Returns:
        URL флага страны
    """
    code = get_country_code(country)
    if code:
        return f"https://flagcdn.com/w40/{code}.png"
    else:
        # Если код не найден, возвращаем дефолтный флаг
        return "https://flagcdn.com/w40/xx.png"
