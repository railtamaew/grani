# Простая инструкция для авторизации
# Этот скрипт показывает пошаговую инструкцию

cat << 'EOF'
╔══════════════════════════════════════════════════════════════╗
║  🔐 Простая инструкция по авторизации                       ║
╚══════════════════════════════════════════════════════════════╝

📋 ШАГ 1: На сервере выполните:
   gcloud auth login --no-browser

📋 ШАГ 2: Команда покажет ссылку. Скопируйте её.

📋 ШАГ 3: На вашем MacBook откройте терминал и выполните:
   (вставьте скопированную команду с --remote-bootstrap=...)

   Или просто откройте URL в браузере:
   (скопируйте URL из команды и вставьте в адресную строку)

📋 ШАГ 4: В браузере:
   - Авторизуйтесь через Google аккаунт (rail.tamaew@gmail.com)
   - После авторизации скопируйте код из URL (параметр code=...)

📋 ШАГ 5: На сервере вставьте код в формате:
   state=XXX&code=YYY

📋 ШАГ 6: Готово! Проверьте:
   gcloud auth list

═══════════════════════════════════════════════════════════════

💡 Альтернатива: Если у вас установлен gcloud на MacBook:

   1. На сервере: gcloud auth login --no-browser
   2. Скопируйте команду с --remote-bootstrap=...
   3. На MacBook выполните эту команду
   4. Скопируйте результат и вставьте на сервере

═══════════════════════════════════════════════════════════════

EOF
